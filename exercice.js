function toggleHiddenInfo1() {
    var hiddenInfo = document.getElementById("hiddentips1");
    if (hiddenInfo.style.display === "none") {
        hiddenInfo.style.display = "block";
    } else {
        hiddenInfo.style.display = "none";
    }
}
